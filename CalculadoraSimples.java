public class CalculadoraSimples{
	public static void main(String[] args){
		int a = 10;
		int b = 2;

		System.out.println("A soma entre " + a + " e " + b + " resulta o valor: " + (a + b));
		System.out.println("A subtração entre " + a + " e " + b + " resulta o valor: " + (a - b));
		System.out.println("A multiplicação entre " + a + " e " + b + " resulta o valor: " + a * b);
		System.out.println("A divisão entre " + a + " e " + b + " resulta o valor: " + a / b);
		System.out.println("O resto da divisão entre " + a + " e " + b + " resulta o valor: " + a % b);
	}
}